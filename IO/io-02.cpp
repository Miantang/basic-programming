#include <iostream>
using namespace std;
int main(){
  int left, right;
  cin >> left >> right;
  cout << left << " + " << right << " = " << left+right << "\n"
    << left << " - " << right << " = " << left-right << "\n"
    << left << " * " << right << " = " << left*right << "\n"
    << left << " / " << right << " = " << left/right <<endl;
  
}